#include <stdio.h>

/**
 * _isupper - check the code
 * @c: call in the integer
 *
 * Return: 1 if success otherwise all lowercase
 */

int _isupper(int c)
{
	char c;

	if (c = 'A')
	{
		printf("%c: %d\n", c, _isupper(c));
	}
	else
	{
		printf("%c: %d\n", c, _isupper(c));
	}
	return (0);
}
